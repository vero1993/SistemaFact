     private void cargarObjeto() {
        //Todos los objetos 
        this.su.getUsuario().setCed_per(txtCedula.getText());
        this.su.getUsuario().setApe_per(txtApellidos.getText());
        this.su.getUsuario().setNom_per(txtNombres.getText());
        this.su.getUsuario().setTel_per(txtTelefono.getText());
        this.su.getUsuario().setEma_per(txtEmail.getText());
        this.su.getUsuario().setDir_per(txtDireccion.getText());
        
        this.su.getUsuario().setEst_per("ACTIVO");
//        this.su.getUsuario().setCla_usu(txtClave.getText());
        
        if (rdbMasculino.isSelected()) {
            this.su.getUsuario().setSex_per("M");
        } else {
            if (rdbFemenino.isSelected()) {
                this.su.getUsuario().setSex_per("F");
            }
        }
         if (rdbGerente.isSelected()) {
            this.su.getUsuario().setTip_usu("G");
        } else {
            if (rdbEmpleado.isSelected()) {
                this.su.getUsuario().setTip_usu("E");
            }
        }
         if(this.foto.isEmpty() == false) //si la foto no esta vacia
        {
        this.su.getUsuario().setFoto(Utilidades.reemplazarCadena(this.foto, ' ', '_'));
        }
     }
