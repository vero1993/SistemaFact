    private void txtBuscarClienteKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBuscarClienteKeyTyped
        // verifica el tipo de caracter a recibir
        if (cbxBuscarClientes.getSelectedIndex() == 1) {
            Validacion.ValidarLetras(evt);
        } else {
            if (cbxBuscarClientes.getSelectedIndex() > 1) {
                Validacion.validarNumeros(evt);
            }
        }
    }//GEN-LAST:event_txtBuscarClienteKeyTyped

    private void cbxBuscarClientesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbxBuscarClientesActionPerformed
       // seleccionar el criterio de busqueda
        if (cbxBuscarClientes.getSelectedIndex() == 0) {
            txtBuscarCliente.setEditable(false);           
            txtBuscarCliente.setEnabled(false);
            txtBuscarCliente.setText("");
        } else {
            txtBuscarCliente.setEnabled(true);
            txtBuscarCliente.setEditable(true);
            txtBuscarCliente.requestFocus();
            txtBuscarCliente.setText("");
            if (cbxBuscarClientes.getSelectedIndex() == 2) {
                txtBuscarCliente.setEnabled(false);
               // txtBuscarPedido.setText("YYY-MM-DD");
            }
        }
    }//GEN-LAST:event_cbxBuscarClientesActionPerformed

