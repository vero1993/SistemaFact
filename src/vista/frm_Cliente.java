    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
         if (Validacion.requerido(txtCedula, txtCedula.getText()) == true
                && Validacion.requerido(txtApellidos, txtApellidos.getText()) == true
                && Validacion.requerido(txtNombres, txtNombres.getText()) == true
                && Validacion.requerido(txtTelefono, txtTelefono.getText()) == true
                && Validacion.requerido(txtEmail, txtEmail.getText()) == true
                && Validacion.requerido(txtDireccionCliente, txtDireccionCliente.getText()) == true) {//valida q los campos no esten vacios
            if (Validacion.validarCedula(txtCedula.getText()) == true) {//si la cedula ingresada es correcta
                this.cargarObjeto();//cargamos los datos
                if (this.scli.getCliente().getId_persona() == null) {//verificamos si ya tiene id
                    if (new ServicioPersona().obtenerPersonaCedula(txtCedula.getText()) == null){//si la cedula es unica
                        if (rdbMasculino.isSelected()) {
                            this.scli.getCliente().setSex_per("M");
                                 } else {
                                  if (rdbFemenino.isSelected()) {
                                     this.scli.getCliente().setSex_per("F");
                             }
                        }
                        if (this.scli.guardar()== true){//guardamos el objeto5
                            JOptionPane.showMessageDialog(this, "SE HA REGISTRADO CORRECTAMENTE", "OK", JOptionPane.INFORMATION_MESSAGE);
                            this.limpiarCampos();
                            this.irListar();
                            this.cargarTabla();
                            this.setSize(625, 337);
                        }else{
                            JOptionPane.showMessageDialog(this, "NO SE PUEDE REGISTRAR!!", "ERROR", JOptionPane.ERROR_MESSAGE);
                        }
                    } else {
                        JOptionPane.showMessageDialog(this, "YA EXISTE CEDULA!!", "ERROR", JOptionPane.ERROR_MESSAGE);
                        this.limpiarCampos();
                    }
                } else {//editar
                    if (this.scli.modificar() == true) {
                        JOptionPane.showMessageDialog(this, "SE HA MODIFICADO CORRECTAMENTE", "OK", JOptionPane.INFORMATION_MESSAGE);
                        this.limpiarCampos();
                        this.irListar();
                        this.cargarTabla();
                        this.setSize(625, 337);
                    } else {
                        JOptionPane.showMessageDialog(this, "No se ha podido modificado!!", "Error", JOptionPane.ERROR_MESSAGE);
                    }
                }
            } else {
                JOptionPane.showMessageDialog(this, "La cedula es incorrecta!!", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

