    private void btnImprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnImprimirActionPerformed
        String usuario = (Sesiones.getCuenta().getUsu().getApe_per() + " " + Sesiones.getCuenta().getUsu().getNom_per());
        ReporteFactura.reporteFactura(this.sFactura.getFactura(), usuario);
        this.tblFacturas.clearSelection();
        this.btnImprimir.setEnabled(false);
        this.btnCancelar.setEnabled(false);
        this.cbxCriterio.setSelectedIndex(0);
    }//GEN-LAST:event_btnImprimirActionPerformed

    private void cbxCriterioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbxCriterioActionPerformed
        if (cbxCriterio.getSelectedIndex() == 0) {
            // txtBuscarCuenta.setEditable(false);
            txtBusqueda.setEnabled(false);
            txtBusqueda.setText("");
        } else {
            txtBusqueda.setEnabled(true);
            //txtBuscarCuenta.setEditable(true);
            txtBusqueda.requestFocus();
            txtBusqueda.setText("");

            if (cbxCriterio.getSelectedIndex() == 2) {
                txtBusqueda.setEnabled(true);
                txtBusqueda.requestFocus();
                txtBusqueda.setText("");
            }
        }
    }//GEN-LAST:event_cbxCriterioActionPerformed

    private void txtBusquedaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtBusquedaActionPerformed
         // habilitar el txt
        if (cbxCriterio.getSelectedIndex() == 2) {
            txtBusqueda.setEnabled(true);
            txtBusqueda.setText("");
            txtBusqueda.requestFocus();
        }
    }//GEN-LAST:event_txtBusquedaActionPerformed

    private void txtBusquedaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBusquedaKeyReleased
        Validacion.validarMayusculas(evt, txtBusqueda);
        //Buscar los pedidos
        if (cbxCriterio.getSelectedIndex() == 1) {
           
            this.modeloTablaFacturas.setLista(this.sFactura.buscarxNumFactura(this.txtBusqueda.getText()));
        } else{ if (cbxCriterio.getSelectedIndex() == 2) {
          
            this.modeloTablaFacturas.setLista(this.sFactura.buscarporCedula(this.txtBusqueda.getText()));
        } 
        }
        this.tblFacturas.setModel(this.modeloTablaFacturas);
        this.tblFacturas.updateUI();
    }//GEN-LAST:event_txtBusquedaKeyReleased

    private void txtBusquedaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBusquedaKeyTyped
             if (cbxCriterio.getSelectedIndex() == 1) {
            Validacion.validarNumeros(evt);
        } else {
            if (cbxCriterio.getSelectedIndex() > 1) {
                Validacion.ValidarLetrasEspacio(evt);
                Validacion.cambiarMayusculas(evt);
            }
        }
    
    }//GEN-LAST:event_txtBusquedaKeyTyped

    private void CargarTabla() {//metodo q permite cargar a la tabla la lista de creditos activos
        this.modeloTablaFacturas.setLista(this.sFactura.listar());//el modelo recive la lista
        this.tblFacturas.setModel(this.modeloTablaFacturas);//aplica el modelo
        this.tblFacturas.updateUI();//asigna los objetos a la tabla
    }

